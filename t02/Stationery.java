package javase02.t02;

public class Stationery {
    
	String productType;
	String producerName;
	int quantity;
	float price;

    // конструктор
    public Stationery(String producerName, String producerName, int quantity, float price) {
        this.productType = productType;
        this.producerName = producerName;
		this.quantity = quantity;
		this.price = price;
    }
}